-- OnStart
-------------------------------------------

-- 起動直後はbootシーンが呼ばれます。
function boot_OnStart()
	-- 1.まず画像を読み込む
	G.sample = loadGraphic("sample1.png");
	
	-- 2.アクターを表示する。
	-- createActor(グラフィックハンドル, X座標, Y座標, レイヤー番号);
	-- レイヤー番号は0から11までの12枚で、数字が大きいほど上になります。
	A.sample = createActor(G.sample, 300, 300, 10);
	
end

